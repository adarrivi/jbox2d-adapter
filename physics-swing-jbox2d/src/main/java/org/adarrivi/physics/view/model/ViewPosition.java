package org.adarrivi.physics.view.model;

public class ViewPosition {

    private int x;
    private int y;

    public ViewPosition(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
